// Cadenas de Texto

/* Las cadenas de texto se pueden definir con 
comillas dobles, 
comillas simples 
o comillas invertidas
*/

console.log("Hello World!, doble")
console.log('Hello World!, simple')
console.log(`Hello World!, invertida`)

// Tipos de Datos

console.log("5")
console.log(5)

// Operaciones Aritméticas
console.log(5 + 2) // Suma
console.log(5 - 2) // Resta
console.log(5 * 2) // Multiplicación
console.log(5 / 2) // División
console.log(5 % 2) // Módulo
console.log(5 ** 2) // Potencia